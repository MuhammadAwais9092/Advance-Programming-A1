# Chapter 6 Exercise 1: Basic Math

# Importing the math library
import math

# Finding the ceil of a
ceil_a = math.ceil(2.3)
print(f"Ceil of 2.3: {ceil_a}")

# Finding the floor of a
floor_a = math.floor(2.3)
print(f"Floor of 2.3: {floor_a}")

# Finding the factorial of a
factorial_a = math.factorial(5)
print(f"Factorial of 5: {factorial_a}")

# Finding the value of 2^3
power_a = math.pow(2, 3)
print(f"Value of 2^3: {power_a}")

# Finding the square root of a
sq_rt_a = math.sqrt(16)
print(f"Square root of 16: {sq_rt_a}")
